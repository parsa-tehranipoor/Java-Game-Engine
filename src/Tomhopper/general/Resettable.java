package TomHopper.general;

/**
 * Interface that gives a method to an object that allows it to be "reset". This
 * definition can be changed by the user so that the "reset" can be whatever
 * they like.
 * @author cdwan
 */
public interface Resettable {
    
    /**
     * A method that "resets" the object.
     */
    public void reset();
    
}
